int memo[61][61][61];
class MutaliskEasy {
  public:
  int f(int a, int b, int c) {
    if(a<=0&&b<=0&&c<=0) return 0;
    if(memo[a][b][c]>=0) return memo[a][b][c];
//    dump3(a,b,c);
    VI X={1,3,9};
    
    int res=200;
    do {
      int a2=max(a-X[0],0),b2=max(b-X[1],0),c2=max(c-X[2],0);
      res = min(res, f(a2,b2,c2)+1);
    } while(next_permutation(X.begin(),X.end()));
    
    return memo[a][b][c]=res;
  }
  int minimalAttacks(vector<int> x) {
    MINUS(memo);
    int a=x[0],b=SZ(x)<2?0:x[1],c=SZ(x)<3?0:x[2];
    return f(a,b,c);
  }
};

